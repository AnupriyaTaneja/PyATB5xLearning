for i in range(101):  # 0 to 100
    if i % 2 == 0:
        print(i)

# |i| |Condition||o/p|
# |0| |0%2 ==0 | |o/p -> True = 0|
# |1| |1%2 ==1 | |o/p -> False -> Nothing will be printed|
# |2| |2%2 ==0 | |o/p -> True = 2|
# |3| |3%2 ==1 | |o/p -> False -> Nothing will be printed|

# Output: # All even numbers
# 0
# 2
